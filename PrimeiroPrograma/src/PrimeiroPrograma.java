public class PrimeiroPrograma  {
    public static void main(String[] args) {
        boolean VouF = true;
        byte a;
        char letra = 'F';
        int valor = 4600;
        double x = 3.14;
        a = 20;
        System.out.printf("Valor de VouF (boolean): %b\n", VouF);
        System.out.printf("Valor de a (byte): %d\n", a);
        System.out.printf("O valor de letra (char) é: %c\n", letra);
        System.out.printf("O valor da string valor (int) é igual a: %d\n", valor);
        System.out.printf("O valor de x (double) é igual a: %2.2f\n", x);
    }
}
